public class Patterns {
    public static void main(String[] args) {

        int n = 3;

        for(int a=1;a<=n;a++) { // a= 4 a = 4<=3

            for(int b=1;b<=n;b++) {  // n = 3  b= 1<=3
                System.out.print(a+" ");
               
            }

            System.out.println();
        }

    }
}

