let fruit = "apple";
console.log(fruit.charAt(2)); // Output: "p"
console.log(fruit.charAt(3)); // Output: "l"
console.log(fruit.charAt(4)); // Output: "e"
console.log(fruit.charAt(5)); // Output: "" (empty string, as index 5 is out of bounds)
console.log(fruit.charAt(-1)); // Output: "" (empty string, as negative index is out of bounds)
